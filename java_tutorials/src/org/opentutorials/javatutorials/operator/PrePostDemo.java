package org.opentutorials.javatutorials.operator;

public class PrePostDemo {

	public static void main(String[] args) {
		
		int a = 3;
		System.out.println(a);
		a++;
		System.out.println(a);
		++a;
		System.out.println(a);
		System.out.println(++a);
		System.out.println(a++);
		System.out.println(a);
	}
}
